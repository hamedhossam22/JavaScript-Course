/*
  What is the difference between var, let, const?

  var 
    - can be reassigned | Mutable
    - Variable can be redeclared.
    - Show undefined when we access variable before declaration. 

  let 
    - can be reassigned | Mutable 
    - Variable cannot be redeclared.
    - Show Erorr when we access variable before declaration. 

  const 
    - cannot be reassigned | Immutable
    - Variable cannot be redeclared.
    - Show Erorr when we access variable before declaration. 
*/

