/*
for (initialization; condition; increment/decrement) {
  Code to be executed in each iteration
}
*/

let i = 10;

for (let i = 1; i <= 5; i++) {
  console.log(i);
}

console.log("--------");

for (let i = 10; i > 5; i--) {
  console.log(i);
}

console.log(i);

// 10
// 9
// 8
// ...
// 1

