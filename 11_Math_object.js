// Math Object
console.log(Math);

// sqrt()
console.log(Math.sqrt(9));

// abs
console.log(Math.abs(-5));

// Pow
console.log(Math.pow(3, 3)); // 3 * 3 * 3

// round
console.log(Math.round(4.8)); // 5 or more = up

// ceil
console.log(Math.ceil(4.1)); // up
console.log(Math.ceil(-3.5));

// floor
console.log(Math.floor(4.7)); // down
console.log(Math.floor(-3.2));

// trunc => retrun integer of number
console.log(Math.trunc(1231.8));
console.log(Math.trunc(-3.2));

// Min
console.log(Math.min(4, 2, 5));

// Max
console.log(Math.max(4, 2, 5));

// random
// console.log(Math.random());
console.log(Math.trunc(Math.random() * 10 + 1));
